namespace Common.Interaction {
    public enum ClickInteraction {
        Primary, // Default is left mouse button
        Secondary, // Default is right mouse button
        AlternatePrimary, // Default is shift + left click
        AlternateSecondary, // Default is shift + right click
        DestructivePrimary, // Default is ctrl + left click
        DestructiveSecondary // Default is ctrl + right click
    }
}
